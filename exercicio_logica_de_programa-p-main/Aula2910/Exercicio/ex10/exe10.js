// Faça um Programa que peça a temperatura em graus Celsius, transforme e mostre em graus Fahrenheit >> F = (C * 9/5) + 32

// Temperatura em Celsius
var tempC = parseInt(prompt("Diga qual a  temperatura em graus Celsius? "));

// Converrtendo para Fahrenheit
var tempF = (tempC * 9/5) + 32;

// Resultado
alert("A temperatura em graus Fahrenheit é: " + tempF);
